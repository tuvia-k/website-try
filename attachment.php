<br>כל אדם יכול להבין מהי התקשרות, בראש ובראשונה כי התקשרות היא לא עוד תיאוריה פסיכולוגית או גישה טיפולית. התקשרות היא פשוט ההסבר של מערכת ההפעלה שלנו. של התוכנה האנושית.
<br>ברגע שאנחנו שומעים התקשרות אנחנו מהנהנים בהסכמה: נכון, זה אני, ככה אני מרגיש! בדיוק!
<br>התקשרות מסבירה מה אנחנו צריכים, מה אנחנו עושים כשזה לא ניתן לנו באופן מספק ואיך זה משפיע על עיצוב האישיות שלנו ועל מערכות היחסים שלנו.
<br>העקרון היסודי של ההתקשרות היא שהאדם נולד ומתעצב בתוך קשר, הוא זקוק לקשר, הוא נפצע בקשר ולכן הוא גם יכול להתרפא באופן מלא רק בתוך קשר.
<br>הצורך הזה בקשר הוא מיום הלידה ועד יום הפטירה. אין רגע בחיים בו אנחנו לא זקוקים לקשר בטוח ויציב. במהלך החיים אנחנו לא צועדים מתלות מלאה לעצמאות מלאה אלא מתלות ליכולת להיות בעצמאות וגם בתלות.
<br>אדם בריא יכול לנוע בין עצמאות לתלות. 
<br>כאשר יש קשר חזק, יציב ובריא האדם הבוגר יכול לחוות את רגעי הלבד שלו באופן מופלא ומיטיב, הוא פנוי לממש את עצמו ויכול להיות בקשר מלא עם עצמו ועם צרכיו.
<br>הקשר הוא הבסיס הבטוח של האדם ממנו הוא יוצא אל העולם, חוקר, מתנסה, צומח ומתרחב.
<br>הכחשה של צרכי התלות שלנו מביאה אותנו להתנכרות לחלקים בריאים ביותר בנפשנו ולצרכים פיזיים ורגשיים ראשוניים. התוצאה היא מנגנוני ויסות ופיצוי לא יעילים ולעיתים הרסניים כגון: ניתוק רגשי, דכאון, חרדה, התמכרות, אובססיות והגנות נוקשות אחרות.
<br>יש 3 הבדלים בין התקשרות מבוגרים להתקשרות ילדים :
<br>הראשון הוא שבהתקשרות מבוגרים הרבה פעמים הקשר מוחזק באופן סימלי ואין נוכחות קונקרטית של בן/ בת הזוג ובכל זאת הקשר קיים ומורגש היטב, שלא כמו אצל התינוק שזקוק לנוכחות הקונקרטית של האם רוב הזמן.
<br>השני הראשון הוא שהתקשרות מבוגרים היא הדדית וסימטרית.
<br>השלישי הוא שבהתקשרות מבוגרים ישנה גם מיניות.
<br>כל שאר המרכיבים של קשר בוגר מיטיב זהים לחלוטין לקשר ילדות מיטיב.
<br>התקשרות מתרחשת גם בממד הנפשי – רגשי וגם בממד הפיזי.
<br>הדרך הטובה ביותר להרגעה של תינוק היא החזקה יציבה ומגע רך, גם התקשרות מבוגרים כוללת בתוכה מגע עדין וחם וחיבוק מחזיק.
<br>התקשרות קשורה גם לדאגה, טיפול ואיכפתיות.
<br>אבל בראש ובראשונה התקשרות טובה קשורה ל: מעורבות רגשית, הענות ומושקעות.
