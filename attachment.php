<br>כשיש צורך, רגשי או פיזי, הצורך מובע באופן בהיר ולא מאיים על ידי מי שנזקק כרגע לשני. (במערכת של אם- ילד, מדובר כמובן בתינוק), השני קורא את אות המצוקה ומתגייס להיענות מיטיבה ומותאמת.
<br>הצורך נענה, יש רגיעה, מערכת ההתקשרות נכבית, ויש חזרה לשקט, מנוחה או עשיה של כל אחד מהצדדים.
<br>המערכת הזו גם מספקת את הצרכים של מי שקרא לעזרה, אבל גם מתגמלת את הנענה באופן מופלא, האדם שהצליח לספק את האחר מרגיש קומפוטנטי, בעל יכולת ומזין.
<br>אבל מעל לכל זה ישנו רווח משותף ל2 הצדדים: שניהם מרגישים שהם לא לבד בעולם, שיש מישהו אחד לפחות שאם הם יצטרכו אותו ירצה להיות שם בשבילה.
<br>זה המקום שבו נכנסת האבולוציה :
<br>ברמה האבולוציונית המין האנושי, בראש כל משפחת היונקים, הוא יצור של קשר.
<br>אנחנו חיים במשפחות ולא בבודדים. הקשר הוא אמצעי הישרדותי ואנחנו יודעים שבהיסטוריה האנושית מי שלא היה לו מבוגר שהגן עליו ודאג לצרכיו, לא שרד.
<br>ברמה של הישרדות נפשית, בריאות נפשית וצמיחה, אנחנו יודעים שגם מי שהצליח לשרוד פיזית בקשר מתעלל או מזניח לא צלח באופן מיטבי את משימות ההתפתחות שלו ולכן לא יוכל לחיות חיים בוגרים מלאים.
<br>מושג נוסף שנכנס כאן הוא: ויסות רגשי.
