<h3 class="h"> הקשר כמטפל</h3>
לכולנו פצעי ילדות שמלווים אותנו אל הקשרים שלנו כבוגרים. פצעי הילדות משוחזרים בקשר שוב ושוב הם מאיימים על הקשר ומנהלים אותו, ועולות תחושות של יאוש ורצון לברוח מהקשר אל קשר אחר או אל הלבד הפחות כואב. בעזרת הטיפול פצעי הילדות מוחזקים ברכות  במרחב הזוגי ומקבלים הכרה ומענה המאפשרים ריפוי. 
<br>
תפיסת העולם ההתקשרותית רלוונטית לא רק למרחב הזוגי והמשפחתי, אלא לכלל המטופלים שמגיעים לקליניקה. לפיכך, הקורס מציג אפשרויות חדשות לטיפול בטראומה, דיכאון, חרדה והתמכרויות.
