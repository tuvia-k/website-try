<br>
מי שמתאימים באמת לקורס הם אנשים בצמיחה שמעוניינים לחקור את המרחב ההתפתחותי של המטופל בקליניקה – אבל יותר מזה את הצמיחה של עצמם במרחב האישי וההתקשרותי.
