<br>
הקורס הוא מסגרת חד שנתית להכשרת מטפלים זוגיים. הוא מיועד למטפלים זוגיים ומשפחתיים ותיקים או למטפלים פרטניים שרוצים לבנות קליניקה של טיפול זוגי.
<br>
הקורס מתבסס על תיאוריית ההתקשרות ועל ההשלכות שלה להתקשרות מבוגרים, ומתבסס על גישות מודרניות ומסורתיות ממוקדות התקשרות. 
<br>
התפיסה ההתקשרותית (Attachment) לטיפול זוגי היא לא רק דרך לפתור את הכאוס והתקיעות של הרבה מאוד מהזוגות. זו גישה שמשתמשת בקשר הזוגי ככלי לריפוי אישי, כבסיס לצמיחה ולהתפתחות וכעוגן המספק יציבות כשהחיים מטלטלים אותנו. 
<br>
הזוגות שאנו פוגשים בקליניקה מגיעים עם  ספקות על הקשר, קונפליקטים קשים, שחיקה, ירידה בתשוקה, קשיים בתקשורת ועוד.
<br>
הגישה ההתקשרותית מספקת מבט שמעבר לתוכן, מפת דרכים. היא מעניקה למטפל הבנה בהירה ועמוקה בקשר למה שלא מתאפשר לבני הזוג והוא הגורם האמיתי לסבל ולספיקות.
<br>
ריקון של הקשר, ניתוק, חוסר אמון, מריבות קולניות או מאבקי כוח והתחשבנויות- כל אלו הם רק סימפטומים למאבק של בני הזוג להרגיש בטוחים ואהובים. המאבק החיובי הזה, שהוא הכוח המניע של כל חיבור בין בני זוג לא תמיד מוביל לתוצאות חיוביות.
<br>
בדרך לתוצאה הרצויה הזו נמצאים פצעי ילדות, טראומות, דפוסי חשיבה והגנות, ואלו לא מאפשרים לשני בני הזוג להרגיש קרובים, רגועים ושמחים זה בזו.
<br>
כשהמרחב הזה מאויים והאהבה או היציבות בספק, מופעלות אסטרטגיות הישרדותיות שפוגעות באופן אנוש ביחסים.
<br>
בגישה הומניסטית, מתוך יצירת בסיס בטוח ומכוונן כמטפלים, אנו עוזרים לבני הזוג להבין מדוע הם הלכו לאיבוד בדרך, ולוקחים אותם יד ביד למקום שבו מותר ואף רצוי להיות פגיעים, זקוקים, חשופים ורכים.
<br>
הקורס משלב ידע אינטגרטיבי של הגישות המובילות היום בעולם לטיפול ממוקד רגש והתקשרות. 
<br>
עיקרו של הקורס הוא למידה של  EFT זוגי (Emotionally Focused Therapy, או טיפול ממוקד רגש) מיסודה של סו ג'ונסון. הגישה הוכחה מחקרית ככלי טיפולי יעיל במיוחד, חוצה תרבויות, קשיים ומשברים, ומספקת מענה למנעד רחב של מצוקות ודילמות במרחב הזוגי. EFT הוא כלי למהלך טיפולי ממוקד, יעיל ועמוק, המייצר שינוי החל מהפגישה הראשונה.
<br>
המרחב הזוגי מעורר רגשות עזים בהרבה בהשוואה לקשרים אחרים בחיינו בשל ההפעלה והמעורבות של מערכת ההתקשרות. גישת ה-EFT  כוללת מפה שעוזרת לנו להבין את הרגשות העוצמתיים, ולרתום אותם לטובת יצירת מרחב בטוח וחיוני בתוך הקשר. 
<br>
השיטה מטפלת בשורש הבעיה הזוגית וחושפת את הרובד העמוק של מה שלא עובד בקשר. היא לא רק נותנת כלים לתקשורת יעילה, אלא נוגעת בצרכים העמוקים, עונה על השאלה מדוע הצרכים הללו אינם נענים, ומאפשרת יצירה של מרחב בטוח לצמיחה ולשינוי. 
<br>
בנוסף לEFT  הקורס מרחיב את ארגז הכלים ואת נקודת המבט על הקשר מתוך התבססות על תיאוריות קרובות מעולם ההתקשרות ומהטיפול החווייתי: עם הרחבה והעמקה בראיית העולם ההתקשרותית, הבנת מערכת העצבים ולימוד של כלים טיפוליים רגשיים וחוויתיים מתוך:  AEDP ,תיאוריית הפוליווגאל של פורג'ס , האקומי ו-IFS. 
<br>
הנחת היסוד של הגישה ההתקשרותית היא שהאדם נוצר, מתפתח ומתקיים לכל אורך החיים בתוך קשר ולא כיחידה נפרדת וזאת בהתאמה מלאה לתפיסה המערכתית הרואה את היחיד כפועל ומתהווה כחלק ממערכת. הנחה זו מסתעפת להבנה מורכבת של הנפש האנושית כאורגניזם שמתהווה בתוך אינטראקציות מפתח בילדות. אינטראקציות אלו משפיעות באופן מהותי על תפיסת העצמי, האחר והעולם, על יכולת ויסות, חוסן, פתיחות לחוויה, אסטרטגיות התמודדות ומוטיבציה. 
<br>
לפיכך, השינוי הקורה בטיפול הוא לא רק במרחב הזוגי, אלא בתפיסת העצמי, בתפיסת האחר ובדרכי הפעולה בעולם של כל אחת ואחד. פצעי הילדות ננגעים בהקשר של קשר בטוח ומוחזקים ברכות. פגיעות מוזמנת לחדר וחוויה חדשה נוצרת. חוויה לא מוכרת, מרפאת, מרגשת.
