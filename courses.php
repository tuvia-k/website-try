<h3 class="h">מה לומדים</h3>
בוגרי הקורס מקבלים כלי עבודה כולל ורחב לעבודה עם זוגות בקליניקה  אשר משיג תוצאות יציבות ומיטיבות למשך שנים ארוכות בחיי המטופלים ובקשר הזוגי שלהם. השיטה מזהה, חוקרת ופותרת מגוון בעיות במרחב הזוגי בזכות החתירה הקבועה שלה להתמודד עם סוגיות הליבה שמצויות בבסיס הקשר הזוגי, ולא רק בדרמה הנקודתית של המשבר והסכסוך הזוגי העכשווי. 
<br>
הקורס מעניק למטפלים מפת התמצאות, המבוססת על תאוריית ההתקשרות, להבנה של כלל החוויות, הרגשות וההתנהגויות האנושיות בתוך קשרים משפחתיים, זוגיים ואחרים. "המקראה" הזו, מפת הנפש האנושית, מהווה בסיס בטוח למטפל ומאפשרת לו להבין מה קורה בחדר ומה מוביל לריפוי של הקשר ושל היחיד בתוך הקשר.  
<br>
הטיפול ההתקשרותי חותר להגיע אל מעבר לתכנים ולסיפורים המפלגים בין בני הזוג. הוא נוגע בצורכי הליבה האנושיים, ומאפשר למטופלים לייצר מערכת יציבה שמסוגלת לעמוד בסופה ומשבר.
<br>
הקורס כולל ידע תיאורטי נרחב על התקשרות, על רגשות, על מערכת העצבים ותפקידם בקשר הזוגי. הידע מתבסס על גישותיהם של סו ג'ונסון, דיאנה פושה, בולבי, רוג'רס, ויניקוט, מינושין , בואן , ניופלד ופורג'ס.
